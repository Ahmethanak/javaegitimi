package ahmethan;

public class while_yapisi {

	public static void main(String[] args) {
	
		
		
		/*10'a_kadar_olan_sayılar */
		int i = 1;
		while (i<10) {
			System.out.println(i);
			i++;
		}
		System.out.println("İlk döngü Bitti...");
		
		
		
		/*10'a_kadar_olan_tek_sayılar */
		int a = 1;
		while (a<10) {
			System.out.println(a);
			a+=2;
		}
		

	}

}
